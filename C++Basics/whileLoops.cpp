#include<bits/stdc++.h>
using namespace std;

int main(){

    int x = 10;
    while(x > 0){
        cout << "Elijah is a genius " << x << endl;
        x = x -1;
    }


    //Do while Loop, allows you to perform an action for a least the first time before checking the condition for it to perform
    do {
        cout << "Google! Elijah is coming";
    }
    while(x > 10);

    return 0;
}